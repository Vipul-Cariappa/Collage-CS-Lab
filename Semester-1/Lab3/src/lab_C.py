# program to work with complex numbers

n1 = complex(input("Enter a complex number: "))
n2 = complex(input("Enter another complex number: "))

s = n1 + n2
d = n1 - n2
p = n1 * n2

print(f"Sum of given 2 complex numbers is {s}")
print(f"Difference between then given 2 complex numbers is {d}")
print(f"Product of given 2 complex numbers is {p}")
